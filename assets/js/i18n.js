let currentLang = localStorage.getItem('portfolio_lang') || 'pt';

/* --- Tradução --- */
function updateContent() {
  const i18nElements = document.querySelectorAll('[data-i18n]');
  i18nElements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (TRANSLATIONS[currentLang][key]) {
      if (el.tagName !== 'INPUT' && el.tagName !== 'TEXTAREA') {
        el.innerHTML = TRANSLATIONS[currentLang][key];
      }
      if (el.hasAttribute('placeholder')) {
        el.setAttribute('placeholder', TRANSLATIONS[currentLang][key]);
      }
    }
  });

  const heroHeadline = document.getElementById('heroHeadline');
  if (heroHeadline) {
    const baseText = currentLang === 'pt' ? 'Desenvolvedor Full Stack' : 'Full Stack Developer';
    heroHeadline.textContent = baseText;
    if (window.animateHeadlineLetters) window.animateHeadlineLetters();
  }

  const langText = document.getElementById('langText');
  if (langText) {
    langText.textContent = TRANSLATIONS[currentLang].lang_btn;
  }
}

/* --- Idioma --- */
function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('portfolio_lang', lang);
  updateContent();
  window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
}

/* --- Inicialização --- */
document.addEventListener('DOMContentLoaded', () => {
  const langToggle = document.getElementById('langToggle');
  if (langToggle) {
    langToggle.addEventListener('click', () => {
      const nextLang = currentLang === 'pt' ? 'en' : 'pt';
      setLanguage(nextLang);
    });
  }
  
  updateContent();
});
